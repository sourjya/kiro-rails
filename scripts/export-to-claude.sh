#!/usr/bin/env bash
# export-to-claude.sh - generate a native Claude Code setup from kiro-rails Kiro files.
#
# Single source of truth = .kiro/*. Re-run after changing steering/hooks/agents/prompts/skills.
# Produces .claude/{CLAUDE.md, settings.json, hooks/{guard-bash.sh,prompts/}, agents/, commands/, skills/}.
#
# Anything that cannot be translated is reported on stderr, never dropped silently.
#
# Requires jq. See docs/references/kiro-to-claude-compatibility-2026-06-05.md
set -uo pipefail

ROOT="$(git rev-parse --show-toplevel 2>/dev/null || pwd)"
cd "$ROOT"

STEER=".kiro/steering"; HOOKS=".kiro/hooks"; AGENTS=".kiro/agents"; PROMPTS=".kiro/prompts"; SKILLS=".kiro/skills"
MCP_SRC=".kiro/settings/mcp.json"
OUT="${1:-.claude}"
OUT_BASE="$(dirname "$OUT")"   # .mcp.json lives at the project root, beside .claude/

command -v jq >/dev/null 2>&1 || { echo "Error: jq is required for export-to-claude.sh"; exit 1; }
[ -d "$STEER" ] || { echo "Error: $STEER not found. Run from a kiro-rails project root."; exit 1; }

# Path of the generated capability sync ledger, relative to OUT_BASE (so it lands
# at the repo root beside .claude/ on a normal run, and inside the temp tree during
# check-claude-fresh.sh - never clobbering the committed copy). See section 7.
LEDGER_REL="docs/references/kiro-claude-sync-ledger.md"

# Content hash of a source file, used by the sync ledger. This is the git blob id
# (deterministic function of file CONTENT only - no timestamp, no HEAD, no path), so
# the ledger regenerates byte-identically and check-claude-fresh.sh stays stable. A
# changed hash is exactly the signal "this artifact was upgraded"; `git log` of the
# ledger file is the human-readable upgrade history.
blob() { git hash-object "$1" 2>/dev/null | cut -c1-12; }

rm -rf "$OUT/agents" "$OUT/commands" "$OUT/skills"
mkdir -p "$OUT/hooks" "$OUT/agents" "$OUT/commands"

# ── 0) steering cross-ref rewriter (shared by CLAUDE.md and commands) ──────────
# Kiro prompt/steering bodies reference sibling steering docs by their .kiro path,
# e.g. `.kiro/steering/ux-console-idiom.md`. On the Claude side that path is not the
# steering home: every steering doc is concatenated into CLAUDE.md. Left verbatim,
# the reference points at content the agent would have to re-Read instead of the
# copy already in its context - and dangles entirely if .kiro/ was not installed
# alongside .claude/. Rewrite each backticked `.kiro/steering/<f>.md` to name the
# CLAUDE.md section (the doc's H1 heading) it now lives under. The map is built once
# from the steering files themselves, so it can never drift from their real
# headings, and the transform is deterministic - generated output stays byte-stable
# for check-claude-fresh.sh. Only backticked "load this file" references are
# rewritten; bare-name mentions and HTML-comment notes are left as-is.
STEER_SED=$(mktemp)
trap 'rm -f "$STEER_SED"' EXIT
for sf in "$STEER"/*.md; do
  b=$(basename "$sf")
  # The doc's first markdown H1 is its CLAUDE.md section title; fall back to the
  # filename if a steering file somehow lacks one.
  h1=$(grep -m1 '^# ' "$sf" | sed 's/^# //')
  [ -z "$h1" ] && h1="$b"
  # Escape the search path's dots (so `.` is literal, not "any char") and any sed
  # metacharacters in the replacement heading (# is the s-command delimiter).
  esc_path=$(printf '.kiro/steering/%s' "$b" | sed 's/[.]/\\./g')
  esc_h1=$(printf '%s' "$h1" | sed 's/[&#\\]/\\&/g')
  printf 's#`%s`#the "%s" rules in `CLAUDE.md`#g\n' "$esc_path" "$esc_h1" >> "$STEER_SED"
done

# ── 1) CLAUDE.md: steering concatenation (overrides first) ─────────────────────
{
  echo "# Project Rules (generated from kiro-rails steering by scripts/export-to-claude.sh)"
  echo "# Source of truth: .kiro/steering/*.md - edit there and re-run; do not edit this file."
  echo "# Capability sync ledger (what maps to what, and fidelity): $LEDGER_REL"
  echo
  if [ -f "$STEER/user-project-overrides.md" ]; then cat "$STEER/user-project-overrides.md"; printf '\n\n---\n\n'; fi
  for f in $(find "$STEER" -name '*.md' -not -name 'user-project-overrides.md' | sort); do
    cat "$f"; printf '\n\n---\n\n'
  done
} | sed -f "$STEER_SED" > "$OUT/CLAUDE.md"

# ── 2) PreToolUse guard script ─────────────────────────────────────────────────
cp scripts/claude-guard-bash.sh "$OUT/hooks/guard-bash.sh"
chmod +x "$OUT/hooks/guard-bash.sh"

# ── 3) commands from prompts ───────────────────────────────────────────────────
# Claude routes slash commands by their `description` frontmatter, so it must be a
# real one-line summary. Read it from the prompt's own frontmatter; never fall back
# to "first non-empty line" (that yields `---`, `<!--`, or a truncated sentence).

# Print the frontmatter `description:` as a single line, folding YAML block
# scalars (`>` / `|`) into one line. Empty output = no frontmatter description.
fm_desc() {
  # NOTE: awk's `exit` still runs the END block, so every early return must set
  # `printed` or the folded description gets emitted twice.
  awk '
    NR==1 && $0=="---" { fm=1; next }
    fm==1 && $0=="---" { if (blk && val!="") { print val; printed=1 } exit }
    fm==1 && blk==1 {
      if ($0 ~ /^[A-Za-z_][A-Za-z0-9_-]*:/) { print val; printed=1; exit }   # next key ends the block
      sub(/^[ \t]+/, ""); val = (val=="" ? $0 : val " " $0); next
    }
    fm==1 && /^description:/ {
      sub(/^description:[ \t]*/, "")
      if ($0 ~ /^[|>][-+]?$/) { blk=1; val=""; next }             # block scalar follows
      print; printed=1; exit
    }
    END { if (!printed && blk && val!="") print val }
  ' "$1"
}

# Emit the prompt body with its frontmatter block removed, so the generated command
# has exactly one frontmatter block rather than two stacked ones.
strip_fm() {
  awk '
    NR==1 && $0=="---" { fm=1; next }
    fm==1 && $0=="---" { fm=2; next }
    fm!=1
  ' "$1"
}

nodesc=()
for f in "$PROMPTS"/*.md; do
  [ -e "$f" ] || continue
  name=$(basename "$f" .md)
  desc=$(fm_desc "$f")
  if [ -z "$desc" ]; then
    nodesc+=("$name")
    desc="Kiro review prompt: $name"   # placeholder - routing will be poor until fixed
  fi
  { printf -- '---\ndescription: "%s"\n---\n\n' "${desc//\"/\'}"; strip_fm "$f" | sed -f "$STEER_SED"; } > "$OUT/commands/$name.md"
done

# ── 4) agents from .kiro/agents/*.json ─────────────────────────────────────────
# Returns a SPACE-SEPARATED list of Claude tools, because some Kiro tools have no
# single counterpart and are instead subsumed by several. Empty means genuinely
# unmappable - the caller reports those, since a silent drop is a capability loss.
#
#   code      - read-only code inspection. Sits in read-only agents' allowlists
#               (ux-red-team: "Do NOT write or modify any files"). Its Claude
#               analogue is plain read access; it is NOT Edit/Write.
#   knowledge - retrieval over the agent's declared `resources:`, which are file
#               globs. Reading those is Read + Glob.
#
# Both are therefore subsumed by tools those agents already hold, so mapping them
# is a no-op on capability. That is deliberate and load-bearing: the emitted
# frontmatter must not change. See KRL-10.
map_tool() { case "$1" in
  read) echo Read;; write) echo Write;; edit) echo Edit;;
  grep) echo Grep;; glob) echo Glob;; shell) echo Bash;;
  web_search) echo WebSearch;; web_fetch) echo WebFetch;;
  code) echo "Read Grep Glob";; knowledge) echo "Read Glob";;
  *) echo "";; esac; }
droptools=()
for f in "$AGENTS"/*.json; do
  [ -e "$f" ] || continue
  name=$(jq -r '.name' "$f"); desc=$(jq -r '.description' "$f")
  ctools=""
  # `allowedTools` is the restriction and wins over `tools`: an agent may declare a
  # tool it is not permitted to use (code-security-reviewer lists `shell` but does
  # not allow it). Reading `tools` here would hand a sandboxed reviewer Bash.
  for t in $(jq -r '(.allowedTools // .tools // [])[]' "$f"); do
    m=$(map_tool "$t")
    if [ -z "$m" ]; then droptools+=("$name:$t"); continue; fi
    # A subsumed tool expands to several Claude tools, most of which the agent
    # already has. Dedupe on whole-word match so `Read` never matches inside a
    # longer name, and so ordering stays stable for byte-comparable output.
    for one in $m; do
      case " $ctools " in *" $one,"*|*" $one "*) continue ;; esac
      ctools+="${ctools:+, }$one"
    done
  done
  # FAIL CLOSED. A Claude subagent with no `tools:` frontmatter inherits EVERY tool,
  # including Write and Bash. If nothing mapped, a Kiro agent that was deliberately
  # sandboxed (read-only reviewers, red-teamers) would silently become fully
  # privileged. Degrade to Read rather than emit an unrestricted agent.
  if [ -z "$ctools" ]; then
    echo "  Warning: agent '$name' has no Claude-mappable tools; failing closed to 'Read'." >&2
    ctools="Read"
  fi
  # strip_fm: the referenced prompt may carry its own frontmatter (agents often point
  # at a .kiro/prompts/*.md via file://). Inlining it verbatim would stack a second
  # frontmatter block inside the agent body.
  ref=$(jq -r '.prompt // ""' "$f"); body=""
  case "$ref" in
    file://*) p="$AGENTS/${ref#file://}"; [ -f "$p" ] && body=$(strip_fm "$p") ;;
    "") ;; *) body="$ref" ;;
  esac
  { printf -- '---\nname: %s\ndescription: "%s"\n' "$name" "${desc//\"/\'}"
    printf 'tools: %s\n' "$ctools"
    printf -- '---\n\n%s\n' "$body"; } > "$OUT/agents/$name.md"
done

# ── 5) skills (format compatible - copy) ───────────────────────────────────────
[ -d "$SKILLS" ] && cp -r "$SKILLS" "$OUT/skills"

# ── 6) settings.json: translate hooks + add PreToolUse guard + permissions ─────
# Build a JSON array of {type:"command", command:<arg>} from command strings (safe via --arg).
to_entries() {
  local out='[]' c
  for c in "$@"; do
    [ -z "$c" ] && continue
    out=$(jq -c --arg c "$c" '. + [{type:"command", command:$c}]' <<<"$out")
  done
  printf '%s' "$out"
}

# Kiro hooks have two action shapes:
#   then.type == "command"  -> then.command, a shell string
#   then.type == "askAgent" -> then.prompt, an instruction for the agent
# Claude has no "ask the agent" hook type, but it does surface hook stdout to the
# model, so an askAgent prompt is faithfully emulated by a command that prints it.
# The prompt is written to a file and `cat`-ed rather than inlined, because these
# prompts are multi-line and contain quotes, backticks and $ - inlining them into a
# shell string is a quoting minefield.
PROMPTDIR="$OUT/hooks/prompts"
rm -rf "$PROMPTDIR"; mkdir -p "$PROMPTDIR"

# `pre` and `preedit` are both PreToolUse, split by matcher: `pre` gates the Bash
# tool, `preedit` gates the file-writing tools. Claude keys PreToolUse entries by
# matcher, so they cannot share one bucket.
ups=(); post=(); pre=(); preedit=(); stop=(); skipped=(); unmapped=()
# led_hooks accumulates one sync-ledger row per hook, captured INLINE so the ledger
# reflects the export's actual disposition of each hook rather than a re-derivation
# that could drift from this logic. Row: "name<TAB>blob<TAB>target<TAB>fidelity<TAB>note".
led_hooks=()
for h in "$HOOKS"/*.kiro.hook; do
  [ -e "$h" ] || continue
  hn=$(basename "$h" .kiro.hook); hb=$(blob "$h")
  if ! jq empty "$h" 2>/dev/null; then
    skipped+=("$(basename "$h")")
    led_hooks+=("$hn	$hb	(none)	dropped	not valid JSON")
    continue
  fi
  if [ "$(jq -r '.enabled' "$h")" = "false" ]; then
    led_hooks+=("$hn	$hb	(none)	dropped	disabled in .kiro")
    continue
  fi
  hname="$hn"
  wtype=$(jq -r '.when.type' "$h")
  atype=$(jq -r '.then.type // ""' "$h")

  case "$atype" in
    runCommand|command) cmd=$(jq -r '.then.command // ""' "$h") ;;
    # Path is hardcoded to .claude/, NOT "$OUT" - the emitted settings.json must be
    # byte-identical whether generated in place or into a temp dir by
    # check-claude-fresh.sh, which would otherwise bake a /tmp path in and always
    # report STALE. Same reason guard-bash.sh is referenced by a fixed path below.
    askAgent) jq -r '.then.prompt // ""' "$h" > "$PROMPTDIR/$hname.txt"
              cmd="cat .claude/hooks/prompts/$hname.txt" ;;
    *)        cmd="" ;;
  esac
  if [ -z "$cmd" ]; then
    unmapped+=("$hname (then.type=${atype:-none})")
    led_hooks+=("$hname	$hb	(none)	dropped	unsupported then.type=${atype:-none}")
    continue
  fi

  # Fidelity baseline: a runCommand string is copied unchanged (verbatim); an
  # askAgent prompt is emulated by printing it via cat (adapted). The approximated
  # events below override this to lossy.
  mapped=1; htgt=""; [ "$atype" = askAgent ] && hfid=adapted || hfid=verbatim
  case "$wtype" in
    userPromptSubmit)                         ups+=("$cmd");  htgt="settings.json UserPromptSubmit" ;;
    fileEdit|fileEdited|fileSave|postToolUse) post+=("$cmd"); htgt="settings.json PostToolUse (Edit/Write)" ;;
    agentStop|stop)                           stop+=("$cmd"); htgt="settings.json Stop" ;;
    # Kiro gates the shell tool; Claude's equivalent is PreToolUse with matcher Bash.
    preToolUse)
      tt=$(jq -r '(.when.toolTypes // ["shell"])[0]' "$h")
      if [ "$tt" != "shell" ]; then
        unmapped+=("$hname (when.toolTypes=$tt, only 'shell' maps to a Claude matcher)"); mapped=0
      else
        pre+=("$cmd"); htgt="settings.json PreToolUse (Bash)"
      fi ;;
    # No git-commit event in Claude. Approximate with PreToolUse/Bash gated on the
    # hook's stdin payload naming a commit. See compatibility doc, "Kiro pre-commit".
    beforeCommit)
      pre+=("IN=\$(cat); printf '%s' \"\$IN\" | jq -r '.tool_input.command // empty' | grep -qE '(^|[^a-z])git commit|git-commit-push\.sh' && $cmd || echo OK")
      htgt="settings.json PreToolUse (Bash)"; hfid=lossy ;;
    # Kiro fires preTaskExecution once per spec task; Claude has no per-task event.
    # Approximate with PreToolUse on the write tools, gated on the payload's
    # file_path naming a UI file - which is exactly the moment "coding begins" that
    # the ux-preflight-gate prompt scopes itself to. Strictly noisier than Kiro
    # (fires per UI-file write, not per task), but the hook is non-blocking by
    # design, so the cost is repetition, not a false gate. Same fidelity trade as
    # beforeCommit above. See compatibility doc, "Kiro preTaskExecution".
    preTaskExecution)
      preedit+=("IN=\$(cat); printf '%s' \"\$IN\" | jq -r '.tool_input.file_path // empty' | grep -qiE '\\.(tsx|jsx|css|scss)\$' && $cmd || echo OK")
      htgt="settings.json PreToolUse (Edit/Write)"; hfid=lossy ;;
    *) unmapped+=("$hname (when.type=$wtype, no Claude event)"); mapped=0 ;;
  esac
  # The askAgent prompt was staged before we knew whether when.type maps. If it did
  # not, drop the orphan: check-claude-fresh.sh diffs the whole tree, so an
  # unreferenced file is indistinguishable from drift.
  if [ "$mapped" -eq 0 ]; then
    rm -f "$PROMPTDIR/$hname.txt"
    led_hooks+=("$hname	$hb	(none)	dropped	when.type=$wtype has no Claude event")
  else
    led_hooks+=("$hname	$hb	$htgt	$hfid	when=$wtype")
  fi
done

ups_json=$(to_entries "${ups[@]:-}")
post_json=$(to_entries "${post[@]:-}")
pre_json=$(to_entries "${pre[@]:-}")
preedit_json=$(to_entries "${preedit[@]:-}")
stop_json=$(to_entries "${stop[@]:-}")

# ── 6b) MCP: enabled servers -> project-root .mcp.json; autoApprove -> permissions ─
# Claude reads .mcp.json from the project root (not .claude/). Kiro-specific fields
# (disabled, autoApprove) have no place in .mcp.json: disabled servers are omitted,
# and autoApprove tools become settings.json permissions (mcp__<server>__<tool>).
mcp_perms='[]'
if [ -f "$MCP_SRC" ] && jq empty "$MCP_SRC" 2>/dev/null; then
  enabled=$(jq '[ (.mcpServers // {}) | to_entries[] | select(.value.disabled != true) ] | length' "$MCP_SRC")
  if [ "$enabled" -gt 0 ]; then
    jq '{ mcpServers: ( (.mcpServers // {}) | to_entries
          | map(select(.value.disabled != true))
          | map({ key: .key,
                  value: (.value | {command, args, env, type, url, headers}
                          | with_entries(select(.value != null))) })
          | from_entries ) }' "$MCP_SRC" > "$OUT_BASE/.mcp.json"
    echo "  + $OUT_BASE/.mcp.json ($enabled enabled MCP server(s))"
  fi
  mcp_perms=$(jq -c '[ (.mcpServers // {}) | to_entries[]
                       | select(.value.disabled != true) | .key as $n
                       | (.value.autoApprove // [])[] | "mcp__\($n)__\(.)" ]' "$MCP_SRC")
fi

# The cross-repo guard must stay FIRST in the Bash matcher: it can hard-block a
# destructive git call (exit 2), and there is no point running the other pre-hooks
# on a command that is about to be refused.
jq -n --argjson ups "$ups_json" --argjson post "$post_json" --argjson pre "$pre_json" \
      --argjson preedit "$preedit_json" \
      --argjson stop "$stop_json" --argjson mcpperms "$mcp_perms" '
{
  "$schema": "https://json.schemastore.org/claude-code-settings.json",
  hooks: (
    {}
    + (if ($ups|length)  > 0 then {UserPromptSubmit: [ {hooks: $ups} ]} else {} end)
    + {PreToolUse: (
        [ {matcher: "Bash", hooks: ([ {type: "command", command: "bash .claude/hooks/guard-bash.sh"} ] + $pre)} ]
        + (if ($preedit|length) > 0 then [ {matcher: "Edit|Write|MultiEdit", hooks: $preedit} ] else [] end)
      )}
    + (if ($post|length) > 0 then {PostToolUse: [ {matcher: "Edit|Write|MultiEdit", hooks: $post} ]} else {} end)
    + (if ($stop|length) > 0 then {Stop: [ {hooks: $stop} ]} else {} end)
  )
}
+ (if ($mcpperms|length) > 0 then {permissions: {allow: ($mcpperms | unique)}} else {} end)
' > "$OUT/settings.json"

# ── 7) capability sync ledger ──────────────────────────────────────────────────
# A generated, content-addressed map of every .kiro source artifact to its Claude
# target and the fidelity of that translation. Emitted every run as a byproduct of
# the export above (hook rows were captured inline; the rest are derived here from
# the same globs the export itself walks), so it cannot drift from what shipped.
# Written via OUT_BASE like .mcp.json, and diffed by check-claude-fresh.sh.
# Deterministic: hashes are git blob ids of CONTENT, rows are sorted, and no
# timestamp or HEAD is embedded - so a diff on this file names exactly which
# artifacts changed since the last sync, which is the whole point.
LEDGER_OUT="$OUT_BASE/$LEDGER_REL"
mkdir -p "$(dirname "$LEDGER_OUT")"
{
  echo "# Kiro → Claude Capability Sync Ledger"
  echo
  echo "GENERATED by scripts/export-to-claude.sh - do not edit by hand."
  echo "Regenerate: bash scripts/export-to-claude.sh && git add .claude .mcp.json $LEDGER_REL"
  echo "check-claude-fresh.sh verifies this file is in sync with .kiro/ source."
  echo
  echo "Each row maps a .kiro source to its generated Claude target. **Hash** is the git"
  echo "blob id of the source *content*: upgrade a Kiro prompt/steering/agent and its hash"
  echo "changes here, so a \`git diff\` on this file tells a freshly-started Claude exactly"
  echo "which capabilities moved, and \`git log -p $LEDGER_REL\` is the upgrade history."
  echo
  echo "**Fidelity:** \`verbatim\` copied unchanged · \`adapted\` reshaped but complete ·"
  echo "\`lossy\` approximated (semantics differ - see the note) · \`dropped\` not represented"
  echo "on the Claude side."
  echo
  echo "## Steering → CLAUDE.md"
  echo
  echo "| Source (.kiro/steering) | Hash | Claude target | Fidelity | Notes |"
  echo "|---|---|---|---|---|"
  for sf in $(find "$STEER" -name '*.md' | sort); do
    b=$(basename "$sf")
    if [ "$b" = "user-project-overrides.md" ]; then
      note="concatenated first; the only steering file meant to be edited"
    else
      note="concatenated; \`.kiro/steering\` cross-refs rewritten to CLAUDE.md sections"
    fi
    printf '| `%s` | `%s` | `CLAUDE.md` | adapted | %s |\n' "$b" "$(blob "$sf")" "$note"
  done
  echo
  echo "## Prompts → commands (slash-commands)"
  echo
  echo "| Source (.kiro/prompts) | Hash | Claude target | Fidelity | Notes |"
  echo "|---|---|---|---|---|"
  for f in $(find "$PROMPTS" -name '*.md' | sort); do
    n=$(basename "$f" .md)
    printf '| `%s.md` | `%s` | `commands/%s.md` | adapted | frontmatter reduced to `description`; cross-refs rewritten |\n' "$n" "$(blob "$f")" "$n"
  done
  echo
  echo "## Agents → subagents"
  echo
  echo "| Source (.kiro/agents) | Hash | Claude target | Fidelity | Notes |"
  echo "|---|---|---|---|---|"
  for f in $(find "$AGENTS" -name '*.json' | sort); do
    n=$(jq -r '.name' "$f")
    afid=adapted; anote="tools mapped; \`resources\` globs folded into read access"
    for d in "${droptools[@]:-}"; do case "$d" in "$n:"*) afid=lossy; anote="$anote; dropped ${d#*:}";; esac; done
    printf '| `%s` | `%s` | `agents/%s.md` | %s | %s |\n' "$(basename "$f")" "$(blob "$f")" "$n" "$afid" "$anote"
  done
  echo
  if [ -d "$SKILLS" ]; then
    echo "## Skills → skills (verbatim copy)"
    echo
    echo "| Source (.kiro/skills) | Hash | Claude target | Fidelity | Notes |"
    echo "|---|---|---|---|---|"
    for d in $(find "$SKILLS" -mindepth 1 -maxdepth 1 -type d | sort); do
      s=$(basename "$d")
      [ -f "$d/SKILL.md" ] && printf '| `skills/%s/SKILL.md` | `%s` | `skills/%s/` | verbatim | copied as-is |\n' "$s" "$(blob "$d/SKILL.md")" "$s"
    done
    echo
  fi
  echo "## Hooks → settings.json"
  echo
  echo "| Source (.kiro/hooks) | Hash | Claude target | Fidelity | Notes |"
  echo "|---|---|---|---|---|"
  printf '%s\n' "${led_hooks[@]:-}" | sort | while IFS=$'\t' read -r nm hh tg fd nt; do
    [ -z "$nm" ] && continue
    printf '| `%s.kiro.hook` | `%s` | %s | %s | %s |\n' "$nm" "$hh" "$tg" "$fd" "$nt"
  done
  echo
  echo "## Generated (no single .kiro source)"
  echo
  echo "- \`skills/kiro-rails/SKILL.md\` - the discoverable capability index, generated from every command, skill, agent, and steering rule above (KRL-15)."
} > "$LEDGER_OUT"
echo "  + $LEDGER_OUT"

# ── 8) kiro-rails index skill (single discoverable entry point) ────────────────
# The whole toolbox is only useful if an agent can find it. Review capabilities
# ship as slash-commands (explicit /), which the model does not surface on its own,
# and there is no one place that says "here is everything kiro-rails offers, and
# when to reach for each." This generates that place: a single routing skill whose
# description carries discovery triggers, listing every command/skill/agent with a
# one-line when-to-use pulled from each artifact's own frontmatter/description. It
# is derived from the same sources the export already walks, so it cannot drift; it
# routes, it does not duplicate the work of the things it lists.
IDX="$OUT/skills/kiro-rails"
mkdir -p "$IDX"
{
  printf -- '---\nname: kiro-rails\n'
  printf 'description: "%s"\n' "Index of the kiro-rails engineering toolbox in this repo: every review command, spec workflow, subagent, and steering rule, with when to use each. Load this to decide which review or workflow to run, or to discover what kiro-rails offers. Routing only - each entry points at its own command, skill, or agent."
  printf -- '---\n\n'
  echo "# kiro-rails toolbox"
  echo
  echo "GENERATED by scripts/export-to-claude.sh from .kiro/ - do not edit by hand."
  echo "This is a routing index: it points you at the right command, skill, agent, or"
  echo "steering section; it does not do the work itself. The full source→target map"
  echo "with fidelity grades is in \`$LEDGER_REL\`."
